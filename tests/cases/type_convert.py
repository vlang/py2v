def main():
    # Int conversion
    print(int(3.7))
    print(int("42"))
    print(int(-3.9))

    # Float conversion
    print(float(42))
    print(float("3.14"))

    # String conversion
    print(str(42))
    print(str(3.14))
    print(str(True))

    # Bool conversion
    print(bool(1))
    print(bool(0))
    print(bool("hello"))
    print(bool(""))


if __name__ == "__main__":
    main()
