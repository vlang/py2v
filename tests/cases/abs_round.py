def main():
    # Abs
    print(abs(5))
    print(abs(-5))
    print(abs(0))

    # Round
    print(round(3.7))
    print(round(3.2))
    print(round(3.5))

    # Round with precision
    print(round(3.14159, 2))
    print(round(2.71828, 3))

    # Negative abs
    print(abs(-3.14))


if __name__ == "__main__":
    main()
