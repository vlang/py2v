def main():
    # String concatenation
    a = "Hello"
    b = "World"
    c = a + " " + b
    print(c)

    # String multiplication
    d = "ab" * 4
    print(d)

    e = "-" * 10
    print(e)

    # Length
    print(len(a))
    print(len(c))

    # Indexing
    print(c[0])
    print(c[-1])


if __name__ == "__main__":
    main()
