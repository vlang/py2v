def main():
    nums = [3, 1, 4, 1, 5, 9, 2, 6]

    # Min and max
    print(min(nums))
    print(max(nums))

    # Sum
    print(sum(nums))

    # With two arguments
    print(min(10, 20))
    print(max(10, 20))

    # Empty with default
    print(min(5, 3))
    print(max(5, 3))


if __name__ == "__main__":
    main()
