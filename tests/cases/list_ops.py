def main():
    # List concatenation
    a = [1, 2, 3]
    b = [4, 5, 6]
    c = a + b
    print(c)

    # List multiplication
    d = [0] * 5
    print(d)

    e = [1, 2] * 3
    print(e)

    # Length
    print(len(a))
    print(len(c))

    # Index access
    print(c[0])
    print(c[-1])


if __name__ == "__main__":
    main()
