def main():
    # Floor division
    print(17 // 5)
    print(-17 // 5)

    # Modulo
    print(17 % 5)
    print(-17 % 5)

    # Power
    print(2 ** 10)
    print(3 ** 4)

    # Combined
    a = 100
    print(a // 7)
    print(a % 7)
    print(a ** 2)

    # Negative numbers
    print(-5 // 2)
    print(-5 % 2)


if __name__ == "__main__":
    main()
